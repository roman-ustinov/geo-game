export type TranslationDictionary = Record<keyof typeof translations.sk, string>

export function loadTranslations(language: Language): TranslationDictionary {
  return translations[language] ?? translations.sk
}

export function interpolate(template: string, values: Record<string, string | number | null | undefined>): string {
  return template.replace(/\{(\w+)\}/g, (_, key) => String(values[key] ?? ''))
}
